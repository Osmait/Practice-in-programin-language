# Operaciones matemáticas básicas
import random
import math
result = 5 + 3  # Suma
result = 5 - 3  # Resta
result = 5 * 3  # Multiplicación
result = 5 / 3  # División
result = 5 // 3  # División entera (parte entera del resultado)
result = 5 % 3  # Módulo (resto de la división)
result = 5 ** 3  # Potenciación

# Funciones matemáticas incorporadas

result = math.sqrt(16)  # Raíz cuadrada
result = math.exp(2)  # Exponencial (e^2)
result = math.log(10)  # Logaritmo natural (base e)
result = math.log10(100)  # Logaritmo en base 10
result = math.sin(30)  # Seno (en radianes)
result = math.cos(60)  # Coseno (en radianes)
result = math.tan(45)  # Tangente (en radianes)

# Redondeo
result = round(3.14159265, 2)  # Redondear a 2 decimales
result = math.ceil(4.1)  # Redondear hacia arriba
result = math.floor(4.9)  # Redondear hacia abajo

# Valor absoluto
result = abs(-5)

# Comparaciones
result = max(3, 7, 1, 9)  # Máximo valor
result = min(3, 7, 1, 9)  # Mínimo valor

# Generación de números aleatorios

random_number = random.random()  # Número decimal aleatorio entre 0 y 1
random_integer = random.randint(1, 10)  # Número entero aleatorio entre 1 y 10

# Conversión entre tipos numéricos
integer = int(3.5)
float_number = float(5)

# Números complejos
complex_number = 3 + 2
